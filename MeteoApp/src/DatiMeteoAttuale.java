
public class DatiMeteoAttuale {
    public String citta;
    public double temperatura;
    public int umidita;
    public String descrizione;
    public String icona;
    
    public DatiMeteoAttuale(String citta, double temperatura, int umidita, String descrizione, String icona){
        this.citta = citta;
        this.temperatura = temperatura;
        this.umidita = umidita;
        this.descrizione = descrizione;
        this.icona = icona;
    }
}
